export const getProfessionalIcon = (name, kind, size = 14) => {
    if (kind === 'directory') {
        return <Folder size={size} style={{ color: '#dcb67a', fill: '#dcb67a22' }} />;
    }

    const ext = name.split('.').pop().toLowerCase();

    switch (ext) {
        case 'stan':
            return <img src={stanLogo} alt="stan" style={{ width: size, height: size, filter: 'drop-shadow(0 0 2px var(--accent))' }} />;

        case 'py':
            return Icons.Python(size);

        case 'js':
        case 'mjs':
            return Icons.JS(size);

        case 'ts':
            return Icons.TS(size);

        case 'jsx':
        case 'tsx':
            return Icons.React(size);

        case 'html':
        case 'htm':
            return Icons.HTML(size);

        case 'css':
        case 'scss':
            return Icons.CSS(size);

        case 'rs':
            return Icons.Rust(size);

        case 'md':
            return Icons.Markdown(size);

        case 'json':
            return Icons.JSON(size);

        case 'java':
        case 'class':
        case 'jar':
            return <Coffee size={size} color="#f89820" />;

        case 'c':
        case 'h':
            return <FileCode size={size} color="#A8B9CC" />;

        case 'cpp':
        case 'cc':
        case 'cxx':
        case 'hpp':
            return <FileCode size={size} color="#00599C" />;

        case 'go':
            return <FileCode size={size} color="#00ADD8" />;

        case 'sh':
        case 'bash':
        case 'zsh':
            return <Terminal size={size} color="#4EAA25" />;

        case 'png':
        case 'jpg':
        case 'jpeg':
        case 'gif':
        case 'webp':
        case 'svg':
            return <Image size={size} color="#a855f7" />;

        default:
            return <File size={size} style={{ opacity: 0.5 }} />;
    }
};
